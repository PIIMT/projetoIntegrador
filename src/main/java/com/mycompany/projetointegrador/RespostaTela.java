    private void mostrarRespostas() throws Exception{
        try{
            var dao = new QuestaoDAO();
            var rs = dao.obterResposta();
            var rsmd = rs.getMetaData();
            int cols = rsmd.getColumnCount();
            DefaultTableModel model = (DefaultTableModel) respostasTable.getModel();
            String[] colName = new String[cols];
            for(int i=0; i < cols; i++)
                colName[i]=rsmd.getColumnName(i+1);
            model.setColumnIdentifiers(colName);
            String id, Pergunta, Resposta, Pontos;
            while(rs.next()){
                id = rs.getString(1);
                Pergunta = rs.getString(2);
                Resposta = rs.getString(3);
                Pontos = rs.getString(4);
                String[]row = {id, Pergunta, Resposta, Pontos};
                model.addRow(row);
            }
        }catch (Exception ex){
            ex.printStackTrace();
        }
    }

